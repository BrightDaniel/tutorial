#include <stdio.h>

int main()
{
	//Arithemetic opertors in C:
	// +
	// -
	// /
	// *
	// %
	// --
	// ++
	//

	int a = 10, b = 5, c;

	//addition in C
	c = a + b;

	printf("a + b = %d \n", c);

	//subtraction in C
	c = a - b;

	printf("a - b = %d \n", c);

	//multtiplication in C
	c = a * b;

	printf("a * b = %d \n", c);

	//division in C
	c = a / b;

	printf("a / b = %d \n", c);

	//modulos in C
	c = a % b;

	printf("a %% b = %d \n", c);
	





}
